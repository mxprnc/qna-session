def build_overall_html(num, qa_list):
    overall_file = os.path.join(WORKING_DIR, "qna{}-overall.html".format(num))
    
    topic_data = compile_overall_by_topics(qa_list)
    
    left_toc = ""
    right_toc = ""
    summary_cards = ""
    
    topic_ids = {
        "framework": "topic-framework",
        "features": "topic-features",
        "monetization": "topic-monetization",
        "environment": "topic-environment",
        "uncategorized": "topic-uncategorized"
    }
    
    for key, info in topic_data.items():
        if not info["blocks"]:
            continue
            
        topic_id = topic_ids[key]
        clean_title = info["title"]
        
        left_toc += '<li><a href="#{}">{}</a></li>\n'.format(topic_id, clean_title)
        right_toc += '<li class="toc-group-title">{}</li>\n'.format(clean_title)
        
        block_htmls = []
        for b_idx, b in enumerate(info["blocks"]):
            block_id = "{}-b{}".format(topic_id, b_idx)
            
            # Format indentation padding-left according to raw heading depth (3, 4, 5)
            # depth 3 -> 8px, depth 4 -> 20px, depth 5 -> 32px
            pad_left = (b["depth"] - 3) * 12 + 8
            
            short_h = b["header"]
            if len(short_h) > 22:
                short_h = short_h[:22] + "..."
            right_toc += '<li><a href="#{}" style="padding-left: {}px;">{}</a></li>\n'.format(block_id, pad_left, short_h)
            
            block_htmls.append("""
            <div style="margin-bottom: 32px;" id="{}">
                <h3 style="margin-top: 16px;">{}</h3>
                <div style="margin-top: 10px;">{}</div>
            </div>
            """.format(block_id, b["header"], b["content_html"]))
            
        summary_cards += """
        <div class="summary-section" id="{}">
            <div class="sec-header">
                <h2>{}</h2>
            </div>
            <div class="sec-body">
                {}
            </div>
        </div>
        """.format(topic_id, clean_title, "\n".join(block_htmls))
        
    title = "Q&A 세션 {} 종합 요구사항 기술 사양서".format(num)
    rendered = generate_html_string(title, left_toc, summary_cards, right_toc, is_overall=True)
    
