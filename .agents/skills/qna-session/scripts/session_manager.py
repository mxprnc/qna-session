    overall_md += "## 📋 종합 목차 (TOC)\n\n"
    for key, info in topic_data.items():
        if info["blocks"]:
            anchor = key
            overall_md += "* [{}](#{})\n".format(info["title"], anchor)
    overall_md += "\n---\n\n"
    
    for key, info in topic_data.items():
        if not info["blocks"]:
            continue
        overall_md += "## <a name=\"{}\"></a>{}\n\n".format(key, info["title"])
        for b in info["blocks"]:
            overall_md += "### {}\n\n".format(b["header"])
            overall_md += "{}\n\n".format(b["content_md"])
        overall_md += "---\n\n"
        
    overall_md += "📢 *Generated by Agent CLI Export Manager*"
    return overall_md

