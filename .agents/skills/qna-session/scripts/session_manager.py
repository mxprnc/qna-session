def get_unique_filepath(filepath):
    if not os.path.exists(filepath):
        return filepath
    dir_name = os.path.dirname(filepath)
    base_name = os.path.basename(filepath)
    name, ext = os.path.splitext(base_name)
    num = 1
    while True:
        new_name = "{}-{}{}".format(name, num, ext)
        new_path = os.path.join(dir_name, new_name)
        if not os.path.exists(new_path):
            return new_path
        num += 1

def resolve_output_path(output_arg, default_filename):
    if not output_arg:
        return get_unique_filepath(os.path.join(WORKSPACE_ROOT, default_filename))
    
    full_path = output_arg
    if not os.path.isabs(full_path):
        full_path = os.path.join(WORKSPACE_ROOT, full_path)
        
    if os.path.isdir(full_path):
        full_path = os.path.join(full_path, default_filename)
        
    parent_dir = os.path.dirname(full_path)
    if parent_dir and not os.path.exists(parent_dir):
        os.makedirs(parent_dir)
        
    return get_unique_filepath(full_path)

