def append_qna(num, question, answer):
    ensure_working_dir()
    md_file = os.path.join(WORKING_DIR, "qna{}.md".format(num))
    
    q_num = 1
    if os.path.exists(md_file):
        with open(md_file, "r", encoding="utf-8") as f:
            content = f.read()
        matches = re.findall(r"^##\s+Q(\d+)", content, re.MULTILINE)
        if matches:
            q_num = max(int(m) for m in matches) + 1

    qa_block = "## Q{}\n{}\n\n### A\n{}\n\n".format(q_num, question.strip(), answer.strip())
    
    with open(md_file, "a", encoding="utf-8") as f:
        f.write(qa_block)
    print("QNA_APPENDED:{} to qna{}.md".format(q_num, num))

def markdown_to_html_body(md_content):
    import markdown
    
    # Process github-style alerts first
    def alert_repl(match):
        alert_type = match.group(1).upper()
        raw_lines = match.group(2).split('\n')
        content_lines = []
        for l in raw_lines:
            l_strip = l.strip()
            if l_strip.startswith('>'):
                content_lines.append(l_strip[1:].strip())
            else:
                content_lines.append(l_strip)
        content_text = "\n".join(content_lines).strip()
        
        # Parse inner alert contents with markdown
        inner_html = markdown.markdown(content_text, extensions=['fenced_code', 'tables'])
        return '\n\n<div class="alert alert-{}"><strong>[{}]</strong> {}</div>\n\n'.format(alert_type.lower(), alert_type, inner_html)
        
    pattern = r"^>\s+\[!(NOTE|TIP|IMPORTANT|WARNING|CAUTION)\]\n((?:>\s+.*?(?:\n|$))*)"
    processed = re.sub(pattern, alert_repl, md_content, flags=re.MULTILINE)
    
    # Single-line alerts fallback
    pattern_single = r"^>\s+\[!(NOTE|TIP|IMPORTANT|WARNING|CAUTION)\]\n>\s+(.*?)$"
    processed = re.sub(pattern_single, lambda m: '<div class="alert alert-{}"><strong>[{}]</strong> {}</div>'.format(m.group(1).lower(), m.group(1), markdown.markdown(m.group(2).strip())), processed, flags=re.MULTILINE)

    # Pre-process: Auto-align improperly indented fenced code blocks (commonly generated by AI in lists)
    # This strips leading spaces so the parser recognizes them strictly as blocks, not continuation text.
    processed = re.sub(r"^\s+```([a-zA-Z0-9-]*)$", r"```\1", processed, flags=re.MULTILINE)

    html = markdown.markdown(processed, extensions=['fenced_code', 'tables'])
    return html

def parse_md_to_qa_list(md_file):
    if not os.path.exists(md_file):
        return []
    with open(md_file, "r", encoding="utf-8") as f:
        content = f.read()
    
    sections = re.split(r"\n##\s+Q\d+", "\n" + content)
    headers = re.findall(r"\n##\s+Q(\d+)", "\n" + content)
    
    qa_pairs = []
    for idx, sec in enumerate(sections[1:]):
        q_num = headers[idx] if idx < len(headers) else str(idx + 1)
        parts = sec.split("### A", 1)
        raw_question = parts[0].strip()
        answer_md = parts[1].strip() if len(parts) > 1 else ""
        
        # Segment the question paragraph dynamically to separate primary questions from intro chat logs
        q_paragraphs = raw_question.split('\n\n')
        question_md = q_paragraphs[0].strip()
        
        if len(q_paragraphs) > 1:
            rest_of_q = "\n\n".join(q_paragraphs[1:]).strip()
            answer_md = rest_of_q + "\n\n" + answer_md
            answer_md = answer_md.strip()
            
        qa_pairs.append({
            "num": q_num,
            "question_md": question_md,
            "answer_md": answer_md,
            "question_html": markdown_to_html_body(question_md),
            "answer_html": markdown_to_html_body(answer_md)
        })
    return qa_pairs

def cleanse_overall_text(text):
    if not text:
        return ""
        
    # 1. Multi-line pattern matching to clean conversational paragraphs
    text = re.sub(
        r"Gemini\s+Flash\s+웹\s*[채체]킹에\s+문의해본\s+결과.*?(?:---|정리해\s+드릴게요\.?)\s*\n",
        "",
        text,
        flags=re.DOTALL | re.IGNORECASE
    )
    
    # Clean any remaining empty markdown code wrappers
    text = re.sub(r"```markdown\s*```", "", text)
    text = re.sub(r"```\s*```", "", text)
    
    lines = text.split('\n')
    cleaned_lines = []
    
    # List of conversational/chat transition filters in Korean
    fillers = [
        r"네,\s+\*\*충분히\s+가능합니다\*\*",
        r"네,\s+충분히\s+가능합니다",
        r"이를\s+구현하기\s+위해.*정리해\s+드릴게요",
        r"이를\s+구현하기\s+위해.*정리해\s+드립니다",
        r"아래와\s+같이.*정리해\s+드립니다",
        r"핵심\s+요약은\s+다음과\s+같습니다",
        r"자세한\s+내용은.*참고해\s+주시기\s+바랍니다",
        r"질문\s+답변\s+내용을\s+보고",
        r"다음의\s+응답을\s+얻었는데",
        r"문의해본\s+결과",
        r"참고해\s+주시기\s+바랍니다"
    ]
    
    for line in lines:
        stripped = line.strip()
        is_filler = False
        
        # Check if line matches conversational rules
        for pattern in fillers:
            if re.search(pattern, stripped, re.IGNORECASE):
                is_filler = True
                break
                
        # Also drop line if it is just a conversational wrap-up
        if stripped.endswith("정리해 드립니다.") or stripped.endswith("정리해 드릴게요.") or stripped.endswith("정리해 드릴게요"):
            is_filler = True
            
        if not is_filler:
            cleaned_lines.append(line)
            
    result = "\n".join(cleaned_lines).strip()
    result = re.sub(r"^---\s*$", "", result, flags=re.MULTILINE)
    return result.strip()

def assign_block_to_topic(raw_header_line, content, topics, q_num):
    content = cleanse_overall_text(content)
    
    # Determine the heading depth from the number of '#' characters
    depth_match = re.match(r"^(#+)", raw_header_line.strip())
    header_depth = len(depth_match.group(1)) if depth_match else 4
    
    header_clean = raw_header_line.strip().replace('#', '').strip()
    
    # Cleanse leading numeric or serial prefixes (e.g. "3. 🛠️ 최종 점검..." -> "🛠️ 최종 점검...")
    header_clean = re.sub(r"^\d+[\.\)]\s*", "", header_clean)
    header_clean = re.sub(r"^[①-⑩]\s*", "", header_clean)
    header_clean = header_clean.strip()
    
    content_lines = content.strip().split('\n')
    
    if content_lines:
        first_line_clean = content_lines[0].strip().replace('#', '').strip()
        first_line_clean = re.sub(r"^\d+[\.\)]\s*", "", first_line_clean)
        first_line_clean = re.sub(r"^[①-⑩]\s*", "", first_line_clean).strip()
        
        if first_line_clean == header_clean or first_line_clean.startswith(header_clean) or header_clean.startswith(first_line_clean):
            content = "\n".join(content_lines[1:]).strip()
            
    content = cleanse_overall_text(content)
            
    # Do not skip if content is empty but acts as a structural parent holding child headings
    if not content.strip():
        return
    if len(content.strip()) < 10 and not any(l.strip().startswith('#') for l in content.split('\n')):
        return
        
    combined_text = (header_clean + "\n" + content).lower()
    
    best_topic = "uncategorized"
    best_score = 0
    
    for key, topic_info in topics.items():
        if key == "uncategorized":
            continue
        score = 0
        for kw in topic_info["keywords"]:
            if kw in combined_text:
                score += 2
        
        # Override to direct file picker features to the "features" category (Topic 2)
        if key == "features" and any(w in combined_text for w in ["파일 열기", "파일 저장", "picker", "editor", "텍스트 수정", "저장", "탐색", "finder"]):
            score += 15
            
        # Override to direct monetization elements to the "monetization" category (Topic 3)
        if key == "monetization" and any(w in combined_text for w in ["admob", "adsense", "수익", "수익화", "광고", "결제", "애드센스", "애드몹"]):
            score += 15
            
        # Override to direct setup configurations to "environment" (Topic 4)
        if key == "environment" and any(w in combined_text for w in ["설치", "sdk", "환경 설정", "빌드", "ide"]):
            score += 15
        
        # Apply standard weight coefficients from Q-number origins
        if q_num == "1" and key in ["framework", "features"]:
            score += 3
        elif q_num == "2" and key == "monetization":
            score += 5
        elif q_num == "3" and key == "environment":
            score += 5
            
        if score > best_score:
            best_score = score
            best_topic = key
            
    if best_score == 0:
        if q_num == "1":
            best_topic = "framework"
        elif q_num == "2":
            best_topic = "monetization"
        elif q_num == "3":
            best_topic = "environment"
        else:
            best_topic = "uncategorized"
            
    cleaned_header = header_clean
    cleaned_header = re.sub(r"\bQ\d+\b\s*(관련)?\s*(세부\s*사항|세부 사항|내용|기록)?", "", cleaned_header, flags=re.IGNORECASE)
    cleaned_header = cleaned_header.strip()
    
    if not cleaned_header or cleaned_header.lower() in ["기본 가이드라인", "기본 가이드", "기본사항", "개요 및 아키텍처 가이드"]:
        if best_topic == "framework":
            cleaned_header = "아키텍처 개요"
        elif best_topic == "features":
            cleaned_header = "핵심 구현 스펙"
        elif best_topic == "monetization":
            cleaned_header = "수익화 모델 개요"
        elif best_topic == "environment":
            cleaned_header = "플랫폼 가이드라인"
        else:
            cleaned_header = "요약 개요"
            
    topics[best_topic]["blocks"].append({
        "header": cleaned_header,
        "content_md": content,
        "content_html": markdown_to_html_body(content),
        "depth": header_depth
    })

